void UImplicitSurfaceMeshComponent::CalculateVerticesFromAGrid(TArray<FVector> Grid, TArray<FVector>& Verts, TArray<FIntVector>& Triangles, TArray<FVector>& Normals)
{
	float edgeLengthX = (Grid[1] - Grid[0]).Size();
	float edgeLengthY = (Grid[2] - Grid[1]).Size();
	float edgeLengthZ = (Grid[4] - Grid[0]).Size();
	float densities[8];
	FVector normals[8];
	int CubeIndex = 0;
	FVector VertexList[12];
	FVector NormalList[12];
	for (int i = 0; i < Grid.Num(); i++)
	{
		float density = 0.f;

		ImplicitSurfaceFunction(Grid[i].X, Grid[i].Y, Grid[i].Z, density);
		if (density <= 0)
		{
			CubeIndex |= 1 << i;
		}
		densities[i] = (density);

		// Process Normal

		float density1, density2;
		FVector N;
		//GX
		ImplicitSurfaceFunction(Grid[i].X + edgeLengthX, Grid[i].Y, Grid[i].Z, density1);
		ImplicitSurfaceFunction(Grid[i].X - edgeLengthX, Grid[i].Y, Grid[i].Z, density2);
		N.X = (density1 - density2) / edgeLengthX;

		//GY
		ImplicitSurfaceFunction(Grid[i].X, Grid[i].Y + edgeLengthY, Grid[i].Z, density1);
		ImplicitSurfaceFunction(Grid[i].X, Grid[i].Y - edgeLengthY, Grid[i].Z, density2);
		N.Y = (density1 - density2) / edgeLengthY;

		//GZ
		ImplicitSurfaceFunction(Grid[i].X, Grid[i].Y, Grid[i].Z + edgeLengthZ, density1);
		ImplicitSurfaceFunction(Grid[i].X, Grid[i].Y, Grid[i].Z - edgeLengthZ, density2);
		N.Z = (density1 - density2) / edgeLengthZ;
		normals[i] = N.GetSafeNormal();
	}

	if (CubeIndex == 0) return;

	//Find the vertices where the surface intersects the cube
	if (edgeTable[CubeIndex] & 1)
	{
		VertexList[0] =
			VertexInterp(Grid[0], Grid[1], densities[0], densities[1]);
		NormalList[0] =
			VertexInterp(normals[0], normals[1], densities[0], densities[1]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 2)
	{
		VertexList[1] =
			VertexInterp(Grid[1], Grid[2], densities[1], densities[2]);
		NormalList[1] =
			VertexInterp(normals[1], normals[2], densities[1], densities[2]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 4)
	{
		VertexList[2] =
			VertexInterp(Grid[2], Grid[3], densities[2], densities[3]);
		NormalList[2] =
			VertexInterp(normals[2], normals[3], densities[2], densities[3]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 8)
	{
		VertexList[3] =
			VertexInterp(Grid[3], Grid[0], densities[3], densities[0]);
		NormalList[3] =
			VertexInterp(normals[3], normals[0], densities[3], densities[0]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 16)
	{
		VertexList[4] =
			VertexInterp(Grid[4], Grid[5], densities[4], densities[5]);
		NormalList[4] =
			VertexInterp(normals[4], normals[5], densities[4], densities[5]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 32)
	{
		VertexList[5] =
			VertexInterp(Grid[5], Grid[6], densities[5], densities[6]);
		NormalList[5] =
			VertexInterp(normals[5], normals[6], densities[5], densities[6]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 64)
	{
		VertexList[6] =
			VertexInterp(Grid[6], Grid[7], densities[6], densities[7]);
		NormalList[6] =
			VertexInterp(normals[6], normals[7], densities[6], densities[7]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 128)
	{
		VertexList[7] =
			VertexInterp(Grid[7], Grid[4], densities[7], densities[4]);
		NormalList[7] =
			VertexInterp(normals[7], normals[4], densities[7], densities[4]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 256)
	{
		VertexList[8] =
			VertexInterp(Grid[0], Grid[4], densities[0], densities[4]);
		NormalList[8] =
			VertexInterp(normals[0], normals[4], densities[0], densities[4]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 512)
	{
		VertexList[9] =
			VertexInterp(Grid[1], Grid[5], densities[1], densities[5]);
		NormalList[9] =
			VertexInterp(normals[1], normals[5], densities[1], densities[5]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 1024)
	{
		VertexList[10] =
			VertexInterp(Grid[2], Grid[6], densities[2], densities[6]);
		NormalList[10] =
			VertexInterp(normals[2], normals[6], densities[2], densities[6]).GetSafeNormal();
	}

	if (edgeTable[CubeIndex] & 2048)
	{
		VertexList[11] =
			VertexInterp(Grid[3], Grid[7], densities[3], densities[7]);
		NormalList[11] =
			VertexInterp(normals[3], normals[7], densities[3], densities[7]).GetSafeNormal();
	}


	int NewVertexCount = 0;
	char LocalRemap[12];

	for (int i = 0; i < 12; i++)
	{
		LocalRemap[i] = -1;
	}

	int originalVertexCount = Verts.Num();

	// Process vertices
	for (int i = 0; triTable[CubeIndex][i] != -1; i++)
	{
		if (LocalRemap[triTable[CubeIndex][i]] == -1)
		{
			Verts.Add(VertexList[triTable[CubeIndex][i]]);
			Normals.Add(NormalList[triTable[CubeIndex][i]]);
			LocalRemap[triTable[CubeIndex][i]] = NewVertexCount;
			NewVertexCount++;
		}
	}


	// Process Triangles
	for (int i = 0; triTable[CubeIndex][i] != -1; i += 3)
	{
		FIntVector index = FIntVector(
			originalVertexCount + LocalRemap[triTable[CubeIndex][i + 0]],
			originalVertexCount + LocalRemap[triTable[CubeIndex][i + 1]],
			originalVertexCount + LocalRemap[triTable[CubeIndex][i + 2]]
		);

		Triangles.Add(index);
	}

	MarkRenderDynamicDataDirty();
}

FVector UImplicitSurfaceMeshComponent::VertexInterp(FVector p1, FVector p2, float densityP1, float densityP2)
{
	return (p1 + (-densityP1 / (densityP2 - densityP1))*(p2 - p1));
}

void UImplicitSurfaceMeshComponent::OnRegister()
{
	Super::OnRegister();
	GenerateMesh();
}

void UImplicitSurfaceMeshComponent::GenerateMesh()
{
	TriangleVertices.Empty();
	TriangleIndex.Empty();
	TriangleNormals.Empty();
	float stepX = (x_Right - x_Left) / subdivision;
	for (float x = x_Left; x < x_Right; x += stepX)
	{
		float stepY = (y_Front - y_Back) / subdivision;
		for (float y = y_Back; y < y_Front; y += stepY)
		{
			float stepZ = (z_Top - z_Buttom) / subdivision;
			for (float z = z_Buttom; z < z_Top; z += stepZ)
			{
				TArray<FVector> Grid;
				Grid.Add(FVector(x, y, z));
				Grid.Add(FVector(x + stepX, y, z));
				Grid.Add(FVector(x + stepX, y + stepY, z));
				Grid.Add(FVector(x, y + stepY, z));
				Grid.Add(FVector(x, y, z + stepZ));
				Grid.Add(FVector(x + stepX, y, z + stepZ));
				Grid.Add(FVector(x + stepX, y + stepY, z + stepZ));
				Grid.Add(FVector(x, y + stepY, z + stepZ));

				CalculateVerticesFromAGrid(Grid, TriangleVertices, TriangleIndex, TriangleNormals);
			}

		}
	}
}



